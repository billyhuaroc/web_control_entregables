package app.erp.web.Entity.Empresa;

public class reportes {
    private String base_erp = "-";
    private String base_fibox = "-";
    private String acta = "-";
    private String acta_firmada = "-";

    public reportes() {
    }
    
    public String getBase_erp() {
        return base_erp;
    }
    public void setBase_erp(String base_erp) {
        this.base_erp = base_erp;
    }
    public String getBase_fibox() {
        return base_fibox;
    }
    public void setBase_fibox(String base_fibox) {
        this.base_fibox = base_fibox;
    }
    public String getActa() {
        return acta;
    }
    public void setActa(String acta) {
        this.acta = acta;
    }
    public String getActa_firmada() {
        return acta_firmada;
    }
    public void setActa_firmada(String acta_firmada) {
        this.acta_firmada = acta_firmada;
    }


    
}
